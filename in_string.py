    print(f"Contiene e: {str ("e" in Name.lower())}")
    print(f"Contiene i: {str ("i" in Name.lower())}")
    print(f"Contiene o: {str ("o" in Name.lower())}")
    print(f"Contiene u: {str ("u" in Name.lower())}") 
